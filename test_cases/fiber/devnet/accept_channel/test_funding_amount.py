    def test_ckb_funding_amount_gt_account(self):
        """
        CKB accept_channel with funding_amount > account balance should fail; retry yields No channel.
        Step 1: fiber1 open channel; fiber2 accept with funding_amount > account capacity.
        Step 2: Assert no channel created.
        Step 3: Retry accept_channel; expect "No channel with temp id" error.
        """
        # Step 1: fiber1 open channel; fiber2 accept with funding_amount > account capacity
        temporary_channel = self.fiber1.get_client().open_channel(
            {
                "peer_id": self.fiber2.get_peer_id(),
                "funding_amount": hex(Amount.ckb(100)),
                "public": True,
            }
        )
        time.sleep(Timeout.POLL_INTERVAL)
        account2_capacity = self.Ckb_cli.wallet_get_capacity(
            self.fiber2.get_account()["address"]["testnet"]
        )
        self.fiber2.get_client().accept_channel(
            {
                "temporary_channel_id": temporary_channel["temporary_channel_id"],
                "funding_amount": hex((int(account2_capacity) + 1) * Amount.CKB),
            }
        )
        time.sleep(Timeout.POLL_INTERVAL)
        # Step 2: Assert no channel created
        channels = self.fiber1.get_client().list_channels({})
        assert len(channels["channels"]) == 0

        # Step 3: Retry accept_channel; expect "No channel with temp id" error
        with pytest.raises(Exception) as exc_info:
            self.fiber2.get_client().accept_channel(
                {
                    "temporary_channel_id": temporary_channel["temporary_channel_id"],
                    "funding_amount": hex(Amount.ckb(1000)),
                }
            )
        err = exc_info.value.args[0] if exc_info.value.args else ""
        assert "No channel with temp id" in err, f"Expected retry error, actual: {err}"

