    def test_normal_fee(self):
        """
        Topology 1-2-3: fiber1->fiber3 fee 1%, fiber3->fiber1 fee 0.1%; assert fee and balances.
        Step 1: Start fiber3, open channel 1-2 (fee 0.1%) and 2-3 (fiber2 fee 1%).
        Step 2: Send 10 CKB fiber1->fiber3, assert fee = 10 CKB * 1%; send 1 CKB fiber3->fiber1, assert fee = 0.1%.
        Step 3: Assert final channel balances.
        """
        # Step 1: Build topology 1-2-3 with different fees
        self.start_new_fiber(self.generate_account(Amount.ckb(1000)))
        self.open_channel(
            self.fibers[0],
            self.fibers[1],
            Amount.ckb(1000),
            Amount.ckb(1),
            fiber1_fee=TLCFeeRate.DEFAULT,
            fiber2_fee=TLCFeeRate.DEFAULT,
        )
        self.open_channel(
            self.fibers[1],
            self.fibers[2],
            Amount.ckb(1000),
            Amount.ckb(1),
            fiber1_fee=1000000,
            fiber2_fee=TLCFeeRate.DEFAULT,
        )
        time.sleep(5)
        # Step 2: Send 10 CKB fiber1->fiber3, assert fee 1%
        payment_hash = self.send_payment(
            self.fibers[0], self.fibers[2], Amount.ckb(10)
        )
        payment = self.fibers[0].get_client().get_payment(
            {"payment_hash": payment_hash}
        )
        assert payment["fee"] == hex(
            int(Amount.ckb(10) * 1000000 / 1_000_000)
        )
        # Send 1 CKB fiber3->fiber1, assert fee 0.1%
        payment_hash = self.send_payment(
            self.fibers[2], self.fibers[0], Amount.ckb(1)
        )
        payment = self.fibers[2].get_client().get_payment(
            {"payment_hash": payment_hash}
        )
        assert payment["fee"] == hex(
            int(Amount.ckb(1) * TLCFeeRate.DEFAULT / 1_000_000)
        )
        # Step 3: Assert final channel balances
        channels1 = self.fiber1.get_client().list_channels({})
        channels2 = self.fibers[2].get_client().list_channels({})
        assert channels1["channels"][0]["local_balance"] == hex(98100000000)
        assert channels2["channels"][0]["local_balance"] == hex(899900001)

