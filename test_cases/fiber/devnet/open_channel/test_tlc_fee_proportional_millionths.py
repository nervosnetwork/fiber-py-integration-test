    def test_ckb_tlc_fee_proportional_millionths_is_zero(self):
        """
        tlc_fee_proportional_millionths == 0: fiber2-fiber3 channel has zero fee; payment fee is 0.
        Step 1: Open fiber1-fiber2 (default) and fiber2-fiber3 (fee 0), wait CHANNEL_READY.
        Step 2: Send payment fiber1->fiber3, assert payment fee is 0x0.
        """
        account3_private_key = self.generate_account(Amount.ckb(1000))
        new_fiber = self.start_new_fiber(account3_private_key)
        self.fiber2.connect_peer(new_fiber)
        time.sleep(1)
        self.fiber1.get_client().open_channel(
            {
                "peer_id": self.fiber2.get_peer_id(),
                "funding_amount": hex(Amount.ckb(500)),
                "public": True,
            }
        )
        time.sleep(1)
        self.wait_for_channel_state(
            self.fiber1.get_client(), self.fiber2.get_peer_id(),
            ChannelState.CHANNEL_READY, Timeout.CHANNEL_READY,
        )
        fiber2_tlc_fee = TLCFeeRate.ZERO
        self.fiber2.get_client().open_channel(
            {
                "peer_id": new_fiber.get_peer_id(),
                "funding_amount": hex(Amount.ckb(1000)),
                "public": True,
                "tlc_fee_proportional_millionths": hex(fiber2_tlc_fee),
            }
        )
        time.sleep(1)
        self.wait_for_channel_state(
            self.fiber2.get_client(), new_fiber.get_peer_id(),
            ChannelState.CHANNEL_READY, Timeout.CHANNEL_READY,
        )
        payment_hash = self.send_payment(
            self.fiber1, new_fiber, Amount.ckb(100)
        )
        payment = self.fiber1.get_client().get_payment(
            {"payment_hash": payment_hash}
        )
        assert payment["fee"] == "0x0"
