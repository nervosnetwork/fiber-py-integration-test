    def test_tlc_fee_proportional_millionths_default(self):
        """
        tlc_fee_proportional_millionths: none (default); open channels, send payment, assert fee and balance.
        Step 1: Start fiber3, connect, open channel fiber1-fiber2 then fiber2-fiber3 (default fee).
        Step 2: Create invoice on fiber3, send payment from fiber1, wait and assert balance change with 0.1% fee.
        """
        # Step 1: Start fiber3, connect, open two channels with default fee
        account3_private_key = self.generate_account(Amount.ckb(1000))
        new_fiber = self.start_new_fiber(account3_private_key)
        time.sleep(3)
        self.fiber2.connect_peer(new_fiber)
        time.sleep(3)
        self.fiber1.get_client().open_channel(
            {
                "peer_id": self.fiber2.get_peer_id(),
                "funding_amount": hex(Amount.ckb(200)),
                "public": True,
            }
        )
        time.sleep(1)
        self.wait_for_channel_state(
            self.fiber1.get_client(), self.fiber2.get_peer_id(),
            ChannelState.CHANNEL_READY, Timeout.CHANNEL_READY,
        )
        self.fiber2.get_client().open_channel(
            {
                "peer_id": new_fiber.get_peer_id(),
                "funding_amount": hex(Amount.ckb(1000)),
                "public": True,
            }
        )
        time.sleep(1)
        self.wait_for_channel_state(
            self.fiber2.get_client(), new_fiber.get_peer_id(),
            ChannelState.CHANNEL_READY, Timeout.CHANNEL_READY,
        )
        # Step 2: Create invoice, send payment, assert balance (0.1% fee = TLCFeeRate.DEFAULT)
        invoice_balance = hex(Amount.ckb(100))
        payment_preimage = self.generate_random_preimage()
        invoice = new_fiber.get_client().new_invoice(
            {
                "amount": invoice_balance,
                "currency": Currency.FIBD,
                "description": "test invoice generated by node2",
                "expiry": "0xe10",
                "final_cltv": "0x28",
                "payment_preimage": payment_preimage,
                "hash_algorithm": "sha256",
            }
        )
        before_channel_12 = self.fiber1.get_client().list_channels({})
        before_channel_32 = new_fiber.get_client().list_channels({})
        time.sleep(10)
        self.fiber2.get_client().graph_channels()
        self.fiber2.get_client().graph_nodes()
        self.fiber1.get_client().send_payment(
            {"invoice": invoice["invoice_address"]}
        )
        time.sleep(10)
        after_channel_12 = self.fiber1.get_client().list_channels({})
        after_channel_32 = new_fiber.get_client().list_channels({})

        # 0.1% fee => amount + amount * 1000 / 1000000
        expected_debit = int(invoice_balance, 16) + int(
            int(invoice_balance, 16) * TLCFeeRate.DEFAULT / 1_000_000
        )
        assert int(before_channel_12["channels"][0]["local_balance"], 16) - int(
            after_channel_12["channels"][0]["local_balance"], 16
        ) == expected_debit
        assert int(after_channel_32["channels"][0]["local_balance"], 16) - int(
            before_channel_32["channels"][0]["local_balance"], 16
        ) == int(invoice_balance, 16)

    def test_tlc_fee_proportional_millionths_overflow(self):
        """
        tlc_fee_proportional_millionths > int.max: open_channel expects Invalid params.
        Step 1: Call open_channel with overflow value, assert error contains Invalid params.
        """
        with pytest.raises(Exception) as exc_info:
            self.fiber2.get_client().open_channel(
                {
                    "peer_id": self.fiber1.get_peer_id(),
                    "funding_amount": hex(Amount.ckb(1000)),
                    "public": True,
                    "tlc_fee_proportional_millionths": "0x1FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF",
                }
            )
        expected_error_message = "Invalid params"
        assert expected_error_message in exc_info.value.args[0], (
            f"Expected substring '{expected_error_message}' "
            f"not found in actual string '{exc_info.value.args[0]}'"
        )

